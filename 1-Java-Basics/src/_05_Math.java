public class _05_Math {
    public static void main(String[] args) {
        System.out.println("Maximum: "+ Math.max(5,10));
        System.out.println("Minimum: "+ Math.min(5,10));
        System.out.println("Square Root: "+ Math.sqrt(64));
        System.out.println("Absolute: "+ Math.abs(-5.3));
        System.out.println("Random Number: "+ (int)( Math.random()*101 ));
    }
}
